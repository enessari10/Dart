void main() {
  // Final ve Const
  // Final =  Değer atandıktan sonra hafızada yer tutmaz, erişmek istediğimiz anda devreye girer
  // ve o zaman hafızada yer tutmaya başlar.

  final String sehir = "Ankara";
  print(sehir);

  //Const  = Değer atandıktan sonra hafızada yer tutar.
  const String sehir2 = "İstanbul";
  print("$sehir2");
}
